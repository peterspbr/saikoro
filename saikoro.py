def roll_dice(prefectures, start_speed=0.05, end_speed=0.5, steps=20):
    # Hide terminal cursor
    sys.stdout.write("\033[?25l")
    sys.stdout.flush()
    
    try:
        print("Now selecting some prefecture to visit...")

        current_speed = start_speed
        slowdown_factor = (end_speed - start_speed) / steps

        for _ in range(steps):
            selected = random.choice(prefectures)
            sys.stdout.write(f"\rThe next stop is: {selected.ljust(20)}")
            sys.stdout.flush()
            time.sleep(current_speed)
            current_speed += slowdown_factor

        print(f"\r\n\n= The choosen prefecture is {selected}!! =\n")

    finally:
        # Show the cursor again
        sys.stdout.write("\033[?25h")
        sys.stdout.flush()

